    }

    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        return view('users/admins/locations/admin_add_location');
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate(request(), [
            'name' => 'required|max:50|unique:locations,name',
        ]);

        $add = new Location;

        $add->name = $request->name;
        $add->description = $request->description;
        $add->save();

        session()->flash('alert-success', 'A locations has been successfully added to our system.');

        return redirect()->action('BusController@showLocation');
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        //
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $edit = Location::findOrFail($id);
        return view('users/admins/locations/admin_update_location', compact('edit'));
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->validate(request(), [
            'description'=>'max:255',
        ]);

        $add = Location::findOrFail($id);

        $add->name = $request->name;
        $add->description = $request->description;
        $add->save();

        Session()->flash('success', 'A location has updated successfully.');

        return redirect()->back();
    }

